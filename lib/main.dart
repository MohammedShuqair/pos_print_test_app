class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  static const platform = MethodChannel(AppStrings.methodChannel);
  // Get battery level.
  String _result = 'Init State';

  Future<void> _print() async {
    try {
      _result = await platform.invokeMethod<String>(AppStrings.printMethod) ??
          'nothing come back';
    } on PlatformException catch (e) {
      _result = "Failed to print: '${e.message}'.";
    }

    setState(() {});
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            Text(_result),
            TextButton(
              onPressed: _print,
              child: const Text('Print'),
            ),
          ],
        ),
      ),
    );
  }
}
